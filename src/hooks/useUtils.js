export function useKeyboardShortcuts(shortcuts) {
  useEffect(() => {
    const handler = (event) => {
      for (const [key, callback] of Object.entries(shortcuts)) {
        if (event.key === key) {
          callback(event);
        }
      }
    };

    window.addEventListener('keydown', handler);
    return () => window.removeEventListener('keydown', handler);
  }, [shortcuts]);
}